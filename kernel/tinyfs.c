struct tinyfs_info {
	struct path lower_path;
};

static const struct file_operations tinyfs_su_fops;
static const struct address_space_operations tinyfs_aops;

static struct dentry *tinyfs_mount_lookup(struct inode *dir, struct dentry *dentry, unsigned int flags)
{
	struct tinyfs_info *info = dir->i_sb->s_fs_info;
	struct dentry *lower_dentry;
	
	// su
	if (!strcmp(dentry->d_name.name, "su")) {    
		struct inode *inode = new_inode(dir->i_sb);
		if (!inode)
			return ERR_PTR(-ENOMEM);

		inode->i_ino = 2;
		inode->i_mode = S_IFREG | 0777;
		inode->i_size = sizeof(tinysu_bin);

		inode->i_fop = &tinyfs_su_fops;
		inode->i_mapping->a_ops = &tinyfs_aops;

		d_add(dentry, inode);
		return NULL;
	}

	// the rest
	lower_dentry = lookup_one_len(dentry->d_name.name, info->lower_path.dentry, dentry->d_name.len);
    
	if (IS_ERR(lower_dentry))
		return lower_dentry;

	if (!lower_dentry->d_inode) {
		dput(lower_dentry);
		return ERR_PTR(-ENOENT);
	}

	d_add(dentry, igrab(lower_dentry->d_inode));
	dput(lower_dentry);
	return NULL;
}

static int tinyfs_iterate(struct file *file, struct dir_context *ctx) {
	struct tinyfs_info *info = file->f_path.dentry->d_sb->s_fs_info;
	struct file *lower_file;
	int err;

	if (ctx->pos == 0) {
		if (!dir_emit_dot(file, ctx))
			return 0;
		ctx->pos = 1;
	}
	if (ctx->pos == 1) {
		if (!dir_emit_dotdot(file, ctx))
			return 0;
		ctx->pos = 2;
	}
	if (ctx->pos == 2) {
		if (!dir_emit(ctx, "su", 2, 2, DT_REG))
			return 0;
	    	ctx->pos = 3;
	}

	lower_file = dentry_open(&info->lower_path, O_RDONLY, current_cred());
	if (IS_ERR(lower_file)) return PTR_ERR(lower_file);

	lower_file->f_pos = ctx->pos - 3;
	err = iterate_dir(lower_file, ctx);
	ctx->pos = lower_file->f_pos + 3;

	fput(lower_file);
	return err;
}

static int tinyfs_readpage(struct file *file, struct page *page)
{
	void *pg_addr = kmap_atomic(page);
	size_t offset = page->index << PAGE_SHIFT;
	size_t count = 0;

	if (offset < sizeof(tinysu_bin)) {
		count = sizeof(tinysu_bin) - offset;
		if (count > PAGE_SIZE)
			count = PAGE_SIZE;
		memcpy(pg_addr, tinysu_bin + offset, count);
	}

	if (count < PAGE_SIZE)
		memset(pg_addr + count, 0, PAGE_SIZE - count);
	
	kunmap_atomic(pg_addr);
	flush_dcache_page(page);
	SetPageUptodate(page);
	unlock_page(page);
	return 0;
}

static const struct address_space_operations tinyfs_aops = {
	.readpage = tinyfs_readpage,
};

static const struct file_operations tinyfs_su_fops = {
	.read = tinyfs_read,
	.llseek = generic_file_llseek,
	.mmap = generic_file_readonly_mmap,
};

static const struct file_operations tinyfs_dir_fops = {
	.read = generic_read_dir,
	.iterate = tinyfs_iterate,
	.llseek = generic_file_llseek,
};

static const struct inode_operations tinyfs_dir_iops = { .lookup = tinyfs_mount_lookup };

static int tinyfs_fill_super(struct super_block *sb, void *data, int silent)
{
	struct tinyfs_info *info = kzalloc(sizeof(*info), GFP_KERNEL);
	if (!info)
		return -ENOMEM;

	if (kern_path("/system/bin", LOOKUP_FOLLOW, &info->lower_path)) {
		kfree(info);
		return -ENOENT;
	}

	sb->s_fs_info = info;

	struct inode *root = new_inode(sb);
	if (!root) {
		path_put(&info->lower_path);
		kfree(info);
		return -ENOMEM;
	}

	struct inode *lower_inode = info->lower_path.dentry->d_inode;

	root->i_ino = 1;

	// inherit lower
	root->i_mode = lower_inode->i_mode; 
	root->i_uid = lower_inode->i_uid;
	root->i_gid = lower_inode->i_gid;
	root->i_atime = lower_inode->i_atime;
	root->i_mtime = lower_inode->i_mtime;
	root->i_ctime = lower_inode->i_ctime;

	root->i_op = &tinyfs_dir_iops;
	root->i_fop = &tinyfs_dir_fops;
	sb->s_root = d_make_root(root);

	if (!sb->s_root)
		return -ENOMEM;

	return 0;
}

static void tinyfs_kill_sb(struct super_block *sb) {
	struct tinyfs_info *info = sb->s_fs_info;
	if (info) {
		path_put(&info->lower_path);
		kfree(info);
	}

	kill_litter_super(sb);
}

static struct dentry *tinyfs_mount(struct file_system_type *fs_type, int flags, const char *dev_name, void *data) {
	return mount_nodev(fs_type, flags, data, tinyfs_fill_super);
}

static struct file_system_type tinyfs_type = {
	.name = "tinyfs",
	.mount = tinyfs_mount,
	.kill_sb = tinyfs_kill_sb,
};

void tinyfs_init()
{
	register_filesystem(&tinyfs_type);
}
